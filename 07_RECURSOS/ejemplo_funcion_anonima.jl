# Ejemplo temático: Funciones anónimas con ->

# Sumar dos números usando una función anónima
suma = (a, b) -> a + b
println("Suma de 3 y 5: ", suma(3, 5))

# Usar función anónima en map
cuadrados = map(x -> x^2, [1, 2, 3, 4])
println("Cuadrados: $cuadrados")
